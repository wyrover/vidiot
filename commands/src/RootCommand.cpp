#include "RootCommand.h"

#include "Project.h"

namespace command {

//////////////////////////////////////////////////////////////////////////
// INITIALIZATION
//////////////////////////////////////////////////////////////////////////

RootCommand::RootCommand()
    :   wxCommand(true)
    ,   mCommandName(_("Unnamed command"))
{
}

RootCommand::~RootCommand()
{
}

//////////////////////////////////////////////////////////////////////////
// SUBMIT
//////////////////////////////////////////////////////////////////////////

void RootCommand::submit()
{
    model::Project::get().Submit(this);
}

//////////////////////////////////////////////////////////////////////////
// GET/SET
//////////////////////////////////////////////////////////////////////////

wxString RootCommand::GetName() const
{
    return mCommandName;
}

} // namespace